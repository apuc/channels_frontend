<template>
  <aside class="nav d-flex flex-column col-md-2 p-0 bg-light">
    <header class="nav-header">
      <div @mouseover="addMenuVisible = true" @mouseout="addMenuVisible = false">
        <button class="addButton" type="button">
          <v-icon scale="1" class="icon" name="plus-circle"/>
        </button>

        <ul class="dropdown-settings" v-if="addMenuVisible">
          <li v-for="(elem, index) in info" class="dropdown-settings__el" :key="index">
            <button type="button" class="btn btn-link" @click="openModal($event, elem.modalTrigger)">{{elem.name}}
            </button>
          </li>

          <li class="dropdown-settings__el">
            <button class="btn btn-link" type="button" @click="logout">Exit</button>
          </li>
        </ul>
      </div>


      <div class="user">
        <div class="placeholder placeholder_user" v-if="isUserLoading"></div>

        <button type="button" class="btn btn-link" @click="openModal($event, 'editProfile')" v-else>
          <img class="user__avatar-img"
               :src="userData.avatar ? userData.avatar.small : 'https://pp.userapi.com/c846218/v846218892/e9022/hu0wa149Jn0.jpg?ava=1'"
               alt="">
          {{userData.username}}
        </button>
      </div>

      <div class="filters" v-if="channels.length > 0 && groups.length > 0">
        <button type="button" class="btn btn-primary filters__filter" data-filter="all" @click="filter">All</button>

        <button type="button"
                class="btn btn-primary filters__filter"
                data-filter="channel"
                @click="filter"
        >
          <v-icon scale="1" class="icon" name="bullhorn"/>
        </button>

        <button type="button"
                class="btn btn-primary filters__filter"
                data-filter="group"
                ref="btn"
                @click="filter"
        >
          <v-icon scale="1" class="icon" name="folder"/>
        </button>
      </div>
    </header>

    <nav>
      <drag v-for="(channel, index) in channels"
            :transfer-data="channel.channel_id"
            :key="channel.channel_id"
      >
        <NavSection v-if="filters.channelsVisible && !isChannelsLoading"
                    title="Каналы"
                    :type="'channel'"
                    :data="channel"
        />
      </drag>

      <drop v-for="(group, index) in groups"
            @drop="handleDrop(group.group_id, ...arguments)"
            :key="group.group_id"
      >
        <NavSection v-if="filters.groupsVisible && !isGroupsLoading"
                    title="Группы"
                    :type="'group'"
                    :data="group"
        />
      </drop>
    </nav>

    <div class="placeholder" v-if="isChannelsLoading || isGroupsLoading"></div>
  </aside>
</template>

<script>
  import NavSection from './NavSection';
  import {mapGetters, mapMutations, mapActions} from 'vuex';

  export default {
    components: {NavSection},
    computed: {
      ...mapGetters({
        groups: 'groups/groups',
        channels: 'channels/channels',
        userData: 'user/info',
        authStatus: 'auth/authStatus',
        isGroupsLoading: 'groups/isGroupsLoading',
        isChannelsLoading: 'channels/isChannelsLoading',
        isUserLoading: 'user/isUserLoading',
      }),
    },
    data() {
      return {
        info: [
          {
            name: 'Создать канал',
            modalTrigger: 'channel'
          },
          {
            name: 'Создать группу',
            modalTrigger: 'group'
          },
          {
            name: 'Редактировать профиль',
            modalTrigger: 'editProfile'
          },
          {
            name: 'Поиск',
            modalTrigger: 'search'
          }
        ],
        addMenuVisible: false,
        filters: {
          channelsVisible: true,
          groupsVisible: true,
        },
      }
    },
    methods: {
      ...mapMutations({
        setModal: 'modal/SET_MODAL',
        setGroupIdForAddingChannels: 'groups/SET_GROUP_ID_FOR_ADDING_CHANNEL',
      }),
      ...mapActions({
        getNav: 'user/GET_NAV',
        addChannelsToGroup: 'groups/ADD_CHANNELS',
        logout: 'auth/LOGOUT',
      }),
      openModal(e, modalType) {
        e.preventDefault();
        this.setModal(modalType);
      },
      filter(e) {
        const target = e.target;
        const filterType = target.getAttribute('data-filter');

        switch (filterType) {
          case 'all':
            this.filters.channelsVisible = true;
            this.filters.groupsVisible = true;
            break;
          case 'channel':
            this.filters.channelsVisible = true;
            this.filters.groupsVisible = false;
            break;
          case 'group':
            this.filters.channelsVisible = false;
            this.filters.groupsVisible = true;
            break;
          default:
            break;
        }
      },
      async handleDrop(group_id, data, event) {
        await this.setGroupIdForAddingChannels(group_id);
        await this.addChannelsToGroup([data]);
      },
    },
    async created() {
      this.filter = {
        channelsVisible: this.channels.length > 0,
        groupsVisible: this.groups.length > 0,
      };
    },

  }
</script>

