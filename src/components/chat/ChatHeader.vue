<template>
  <header class="bg-light">
    <div class="settings" @mouseover="settingsVisible = true" @mouseout="settingsVisible = false">
      <button type="button" class="settings__btn">
        <v-icon scale="1.6" name="cog"/>
      </button>

      <div class="settings__list" v-if="settingsVisible">
        <ul class="list">
          <li class="list__el" v-for="(setting, index) in settingsList">
            <a :href="setting.link">{{ setting.name }}</a>
          </li>
        </ul>
      </div>
    </div>

    <b-dropdown class="language-dropdown" variant="link">
      <button class="settings__btn" type="button" slot="button-content">
        <v-icon scale="1.6" class="icon" name="flag"/>
      </button>

      <b-dropdown-item
        v-for="lang in $ml.list"
        :key="lang"
        @click="$ml.change(lang)"
        v-text="lang"
      ></b-dropdown-item>
    </b-dropdown>

    <button type="button" class="btn btn-light" @click="openModal">{{channelData.user_count}} {{getNoun(channelData.user_count, 'пользователь', 'пользователя',
      'пользователей')}}
    </button>

    <button class="btn btn-primary exit" type="button" @click="exit">Exit</button>
  </header>
</template>

<script>
  import {mapGetters} from 'vuex';

  export default {
    computed: {
      ...mapGetters({
        channelData: 'channels/channelInfo',
        channels: 'channels/channels',
      }),

    },
    data() {
      return {
        settingsList: [
          {
            name: 'Настройки канала',
            link: 'https://github.com'
          },
          {
            name: 'Настройки чего-нибудь ещё',
            link: 'https://github.com'
          },
          {
            name: 'Настройки профиля',
            link: 'https://github.com'
          },
        ],
        settingsVisible: false,
        usersCount: null
      }
    },
    methods: {
      exit() {
        this.$store.dispatch('auth/LOGOUT');
        this.$router.push('/')
      },
      getNoun(number, one, two, five) {
        number = Math.abs(number);
        number %= 100;
        if (number >= 5 && number <= 20) {
          return five;
        }
        number %= 10;
        if (number === 1) {
          return one;
        }
        if (number >= 2 && number <= 4) {
          return two;
        }
        return five;
      },
      openModal() {
        this.$store.commit('modal/setModal', 'channelUsers');
        this.$store.commit('modal/currentModal', 'channelUsers');
        this.$store.commit('modal/toggleEditMode');
      },
    },
    created() {
      if (!this.channelData.channel_id) {
        window.onload = () => {
          const slug = location.pathname;
          const channelObj = this.channels.find(channel => channel.slug === slug.slice(1));
          this.$store.commit('channels/SET_CHANNEL_DATA', channelObj);
          this.$store.commit('channels/SET_CHANNEL_ID', channelObj.channel_id);
          this.$store.dispatch('channels/GET_USERS', channelObj.channel_id)
        }
      }
    }
  }
</script>

